/*Να γραφεί ένα πρόγραμμα το οποίο να εμφανίζει στην οθόνη όλους τους χαρακτήρες από τον χαρακτήρα με κωδικό 32 μέχρι τον χαρακτήρα με κωδικό 122. 	Τι μετατροπή πρέπει να γίνει στο πρόγραμμα αυτό ώστε να εμφανίζει σε κάθε γραμμή της οθόνης 10 χαρακτήρες;*/
#include <iostream>
using namespace std;
int main(){
	int counter=0;
	for (int i=32;i<=122;i++){
		cout<<char(i);
		if(counter%10==0){
			cout<<endl;
			}
		counter++;
		}
	
	return 0;
	}
